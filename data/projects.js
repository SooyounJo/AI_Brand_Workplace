export function getProject(slug) {
  if (!slug) return null;
  const base = PROJECTS.find((item) => item.slug === slug);
  if (base) return base;
  if (typeof window === "undefined") return null;
  try {
    const raw = window.sessionStorage.getItem("aibrand-custom-projects");
    if (!raw) return null;
    const list = JSON.parse(raw);
    return list.find((item) => item.slug === slug) || null;
  } catch {
    return null;
  }
}

export function saveCustomProject(project) {
  if (typeof window === "undefined") return;
  const raw = window.sessionStorage.getItem("aibrand-custom-projects");
  const list = raw ? JSON.parse(raw) : [];
  const next = [project, ...list.filter((item) => item.slug !== project.slug)];
  window.sessionStorage.setItem("aibrand-custom-projects", JSON.stringify(next));
}

